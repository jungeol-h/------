-- ─── 1. 테이블 정의 ─────────────────────────────────────────
create table if not exists quiz_sets (
  id            text primary key,
  title         text not null,
  grade         text not null,              -- '중1' | '중2' | '중3'
  round         int  not null default 1,
  source        text,
  description   text,
  is_published  boolean default true,
  created_at    timestamptz default now()
);
create index if not exists idx_quiz_sets_grade on quiz_sets(grade);

create table if not exists quiz_questions (
  id                text primary key,
  quiz_set_id       text references quiz_sets(id) on delete cascade,
  order_no          int  not null,
  question          text not null,
  accepted_answers  text[] not null,
  explanation       text,
  hint              text
);
create index if not exists idx_quiz_questions_set on quiz_questions(quiz_set_id, order_no);

create table if not exists quiz_attempts (
  id            text primary key,
  student_id    text references users(id),
  quiz_set_id   text references quiz_sets(id) on delete cascade,
  answers       jsonb not null,
  score         int   not null,
  total         int   not null,
  submitted_at  timestamptz default now(),
  unique (student_id, quiz_set_id)
);
create index if not exists idx_quiz_attempts_set on quiz_attempts(quiz_set_id);
create index if not exists idx_quiz_attempts_student on quiz_attempts(student_id);

-- ─── 1-b. RLS 정책 (베타 기간: anon key 전체 허용 — 기존 테이블과 동일 패턴) ──
alter table quiz_sets       enable row level security;
alter table quiz_questions  enable row level security;
alter table quiz_attempts   enable row level security;

drop policy if exists "anon_all" on quiz_sets;
drop policy if exists "anon_all" on quiz_questions;
drop policy if exists "anon_all" on quiz_attempts;

create policy "anon_all" on quiz_sets       for all to anon using (true) with check (true);
create policy "anon_all" on quiz_questions  for all to anon using (true) with check (true);
create policy "anon_all" on quiz_attempts   for all to anon using (true) with check (true);

